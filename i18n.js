  function get(path, lang) {
    var o = T[lang] || T.de;
    return path.split('.').reduce(function (obj, k) { return obj && obj[k]; }, o);
  }

  function getLang() {
    var param = (function () {
      var m = /[?&]lang=(de|en)/i.exec(window.location.search);
      return m ? m[1].toLowerCase() : null;
    })();
    if (param) return param;
    try {
      return localStorage.getItem('bht-lang') || document.documentElement.getAttribute('lang') || 'de';
    } catch (e) {
      return 'de';
    }
  }

  function setLang(lang) {
    try { localStorage.setItem('bht-lang', lang); } catch (e) {}
    document.documentElement.setAttribute('lang', lang);
    applyLang();
    try {
      document.dispatchEvent(new CustomEvent('bht-lang-change', { detail: { lang: lang } }));
    } catch (e) {}
  }

  function applyLang() {
    var lang = getLang();

    document.querySelectorAll('[data-i18n]').forEach(function (el) {
      var key = el.getAttribute('data-i18n');
      var val = get(key, lang);
      if (val != null) el.textContent = val;
    });

    // data-i18n-html: allows trusted HTML markup in translations (used for styled hero titles)
    document.querySelectorAll('[data-i18n-html]').forEach(function (el) {
      var key = el.getAttribute('data-i18n-html');
      var val = get(key, lang);
      if (val != null) el.innerHTML = val;
    });

    document.querySelectorAll('[data-i18n-placeholder]').forEach(function (el) {
      var key = el.getAttribute('data-i18n-placeholder');
      var val = get(key, lang);
      if (val != null) el.placeholder = val;
    });

    // data-i18n-attr="attrName:key|attrName2:key2" — set arbitrary attributes from translations
    document.querySelectorAll('[data-i18n-attr]').forEach(function (el) {
      var raw = el.getAttribute('data-i18n-attr');
      if (!raw) return;
      raw.split('|').forEach(function (pair) {
        var idx = pair.indexOf(':');
        if (idx === -1) return;
        var attr = pair.slice(0, idx).trim();
        var key = pair.slice(idx + 1).trim();
        var v = get(key, lang);
        if (v != null) el.setAttribute(attr, v);
      });
    });

    document.querySelectorAll('option[data-i18n]').forEach(function (el) {
      var key = el.getAttribute('data-i18n');
      var val = get(key, lang);
      if (val != null) el.textContent = val;
    });

    document.querySelectorAll('.lang-de').forEach(function (el) { el.hidden = lang !== 'de'; });
    document.querySelectorAll('.lang-en').forEach(function (el) { el.hidden = lang !== 'en'; });

    document.querySelectorAll('.lang-switcher [data-lang]').forEach(function (btn) {
      var isActive = btn.getAttribute('data-lang') === lang;
      btn.classList.toggle('is-active', isActive);
      btn.setAttribute('aria-current', isActive ? 'true' : 'false');
    });

    var titleKey = document.querySelector('[data-i18n-title]');
    if (titleKey) {
      var key = titleKey.getAttribute('data-i18n-title');
      var titleVal = get(key, lang);
      if (titleVal) {
        var titleMode = titleKey.getAttribute('data-i18n-title-mode');
        document.title =
          titleMode === 'full' ? titleVal : titleVal + ' | Baltic Health Travel';
      }
    }
  }

  window.BHT = window.BHT || {};
  window.BHT.i18n = {
    T: T,
    get: get,
    getLang: getLang,
    setLang: setLang,
    applyLang: applyLang
  };

  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', function () {
      applyLang();
    });
  } else {
    applyLang();
  }
