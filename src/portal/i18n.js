let lang =
  typeof localStorage !== 'undefined' && localStorage.getItem('bht-lang') === 'en' ? 'en' : 'de';

export function getLang() {
  return lang;
}

export function setLang(next) {
  lang = next === 'en' ? 'en' : 'de';
  try {
    localStorage.setItem('bht-lang', lang);
  } catch (_) {}
}

export function t(key) {
  var bundle = STRINGS[lang] || STRINGS.de;
  return bundle[key] != null ? bundle[key] : key;
}
